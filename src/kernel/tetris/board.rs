const BOARD_UNIT_WIDTH: usize = 10;
const BOARD_UNIT_HEIGHT: usize = 20; // NOTE: 21行目以降を表示しないので20で済ませている

use super::{mino::block::Block, playfield::FieldDrop};

#[derive(Clone, PartialEq, Eq, Debug)]
pub struct Board([[Option<Block>; BOARD_UNIT_WIDTH]; BOARD_UNIT_HEIGHT]);

impl Board {
    pub(super) fn blank() -> Self {
        Board([[None; BOARD_UNIT_WIDTH]; BOARD_UNIT_HEIGHT])
    }

    pub(super) fn demonstrated(&self, drop: FieldDrop) -> Self {
        let mut board_mat = self.0.clone();
        let drop_mat = drop.mino.mold.matrix();

        for (y, line) in drop_mat.iter().enumerate() {
            for (x, exists) in line.iter().enumerate() {
                let (dx, dy) = drop.pos.into();
                let x = x + dx as usize;
                let y = y + dy as usize;

                if let (0..10, 0..20) = (x, y) {
                    board_mat[y][x] = Some(drop.mino.block).filter(|_| *exists);
                }
            }
        }

        Board(board_mat)
    }
}

